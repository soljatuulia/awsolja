package net.tutorit.checkpoint1;

/**
 *
 * @author Solja
 */
public class Microwave implements Merchandise {

    private String name;
    private int price;

    public Microwave(String name, int price) {
        this.name = name;
        this.price = price;
    }

    @Override
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
}
