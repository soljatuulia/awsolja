package net.virkkunen.techniques.advanced;

/**
 *
 * @author jyrki
 */
public class Person {
    private int id;
    private String name;
    
    public Person(String n){
        name=n;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
}
